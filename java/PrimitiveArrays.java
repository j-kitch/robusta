public class PrimitiveArrays {

    public static void main(String[] args) {
        boolean[] boolArray = {true};
        System.out.println(boolArray[0]);

        byte[] byteArray = {1, 2, -3, -40, -20};
        System.out.println(byteArray[0]);

        char[] charArray = { 3 };
        System.out.println(charArray[0]);

        short[] shortArray = { 0 };
        System.out.println(shortArray[0]);

        int[] intArray = { 4312 };
        System.out.println(intArray[0]);

        long[] longArray = { -4321553 };
        System.out.println(longArray[0]);

        float[] floatArray = { -234.567f };
        System.out.println(floatArray[0]);

        double[] doubleArray = { -4321.54322 };
        System.out.println(doubleArray[0]);
    }
}
